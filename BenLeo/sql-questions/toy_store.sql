DROP TABLE IF EXISTS products;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS wishlists;

CREATE TABLE products (
  id SERIAL PRIMARY KEY NOT NULL,
  created_at DATE,
  name VARCHAR(255),
  price FLOAT,
  on_sale BOOLEAN DEFAULT false NOT NULL
);

CREATE TABLE users (
  id SERIAL PRIMARY KEY NOT NULL,
  created_at DATE,
  name VARCHAR(255)
);

CREATE TABLE wishlists (
  id SERIAL PRIMARY KEY NOT NULL,
  created_at DATE,
  user_id INTEGER,
  product_id INTEGER
);

